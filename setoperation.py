# add() add new element
set1 = {1,2,3,4,5}
set1.add(8)
print(set1)

# remove() delete element of set
set1.remove(1)
print(set1)

# pop() remove a random item from the set
fruits = {'apple','banana','cherry'}
fruits.pop()
print(fruits)

# clear() empty the set
set2 = {1,2,3,7,8,9}
set2.clear()
print(set2)

# len() get the length of set
print(len(set1))
# set() mean empty set